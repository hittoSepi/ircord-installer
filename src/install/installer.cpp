constexpr std::uintmax_t kMinimumFreeSpaceBytes = 512ull * 1024ull * 1024ull;

std::string Join(const std::vector<std::string>& values, const std::string& separator) {
    std::ostringstream out;
    for (std::size_t i = 0; i < values.size(); ++i) {
        if (i != 0) {
            out << separator;
        }
        out << values[i];
    }
    return out.str();
}

std::string RandomHex(std::size_t bytes) {
    static constexpr char kHex[] = "0123456789abcdef";
    std::random_device rd;
    std::uniform_int_distribution<int> dist(0, 255);

    std::string output;
    output.reserve(bytes * 2);
    for (std::size_t i = 0; i < bytes; ++i) {
        const auto value = static_cast<unsigned char>(dist(rd));
        output.push_back(kHex[(value >> 4) & 0x0F]);
        output.push_back(kHex[value & 0x0F]);
    }
    return output;
}

std::string BuildServerConfig(const InstallConfig& config,
                              const std::filesystem::path& cert_path,
                              const std::filesystem::path& key_path) {
    std::ostringstream out;
    out << "# IRCord Server Configuration\n";
    out << "# Generated by ircord-installer\n\n";
    out << "[server]\n";
    out << "host = \"" << config.listen_address << "\"\n";
    out << "port = " << config.port_text << "\n";
    out << "log_level = \"info\"\n";
    out << "max_connections = 100\n\n";
    out << "[tls]\n";
    out << "cert_file = \"" << cert_path.string() << "\"\n";
    out << "key_file = \"" << key_path.string() << "\"\n\n";
    out << "[database]\n";
    out << "path = \"" << (std::filesystem::path(config.data_path) / "ircord.db").string() << "\"\n\n";
    out << "[limits]\n";
    out << "max_message_bytes = 65536\n";
    out << "ping_interval_sec = 30\n";
    out << "ping_timeout_sec = 60\n";
    out << "msg_rate_per_sec = 20\n";
    out << "conn_rate_per_min = 10\n";
    out << "commands_per_min = 30\n";
    out << "joins_per_min = 5\n";
    out << "abuse_threshold = 5\n";
    out << "abuse_window_min = 10\n";
    out << "ban_duration_min = 30\n\n";
    out << "[security]\n";
    out << "file_encryption_key = \"" << RandomHex(32) << "\"\n";
    return out.str();
}

std::string BuildSystemdUnit(const InstallConfig& config) {
    const auto install_dir = std::filesystem::path(config.install_path);
    const auto data_dir = std::filesystem::path(config.data_path);

    std::ostringstream out;
    out << "[Unit]\n";
    out << "Description=IRCord Chat Server\n";
    out << "After=network.target\n";
    out << "StartLimitIntervalSec=0\n\n";
    out << "[Service]\n";
    out << "Type=simple\n";
    out << "User=ircord\n";
    out << "Group=ircord\n";
    out << "WorkingDirectory=" << install_dir.string() << "\n";
    out << "ExecStart=" << (install_dir / "ircord-server").string()
        << " --headless --config " << (install_dir / "server.toml").string() << "\n";
    out << "Restart=on-failure\n";
    out << "RestartSec=5\n";
    out << "StandardOutput=journal\n";
    out << "StandardError=journal\n";
    out << "SyslogIdentifier=ircord-server\n";
    out << "NoNewPrivileges=yes\n";
    out << "ProtectSystem=strict\n";
    out << "ReadWritePaths=" << data_dir.string() << "\n";
    out << "ReadOnlyPaths=/etc/letsencrypt /etc/ircord\n";
    out << "PrivateTmp=yes\n\n";
    out << "[Install]\n";
    out << "WantedBy=multi-user.target\n";
    return out.str();
}

bool RunOrCapture(const std::string& command,
                  const std::string& error_prefix,
                  const InstallerRunner::ProgressCallback& callback,
                  std::string* error_out) {
    const auto result = SystemOps::RunShell(command);
    if (result.exit_code == 0) {
        return true;
    }

    if (error_out != nullptr) {
        *error_out = error_prefix + "\n" + result.output;
    }
    callback(error_prefix, true);
    return false;
}

